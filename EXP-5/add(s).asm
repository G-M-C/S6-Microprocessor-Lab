MSG3 DB 0AH,0DH,"THE ANSWER IS:$"
DATA ENDS

CODE SEGMENT
START:
      MOV AX,DATA
      MOV DS,AX

      LEA DX,MSG1
      MOV AH,09H
      INT 21H
      MOV AH,01H
      INT 21H
      MOV CL,AL


      LEA DX,MSG2
      MOV AH,09H
      INT 21H
      MOV AH,01H
      INT 21H
      MOV BL,AL


      LEA DX,MSG3
      MOV AH,09H
      INT 21H

      MOV AH,00H
      ADD BL,CL
      MOV AL,BL
      AAA
      ADD AH,30H
      MOV BH,AH
      ADD AL,30H
      MOV BL,AL

      MOV DL,BH
      MOV AH,02H
      INT 21H

      MOV DL,BL
      MOV AH,02H
      INT 21H

      MOV AH,4CH
      INT 21H

CODE ENDS
END START
